// Comparison operators
let bool1 = 5 > 3;
let bool2 = 5 < 3;
let bool3 = 5 === 5;
let bool4 = 5 !== 5;

console.log(bool1);  
console.log(bool2);  
console.log(bool3);  
console.log(bool4);  

// Variables x and y
let x = 8;
let y = 12;

// Comparison checks
console.log(x > y);  
console.log(x <= y);  
console.log(x === y);  
console.log(x !== y);  

// Logical operations
let a = true;
let b = false;

console.log(a && b);  
console.log(a || b);  
console.log(!a);  

